a, b = 5, 2
print(a + b, a - b, a * b, a / b, a % b)  # Arithmetic
print(a > b, a == b, a != b)  # Comparison
print(a > 0 and b > 0)  # Logical
print(a > 0 or b < 0)  # Logical
print(not (a > 0))  # Logical   
print(a << 1, a >> 1)  # Bitwise
print(a & b, a | b, a ^ b)  # Bitwise
print(~a)  # Bitwise
print(a ** b)  # Exponentiation
print(a // b)  # Floor Division
print(a == b)  # Equality
print(a != b)  # Inequality
print(a is b)  # Identity
print(a is not b)  # Identity
print(a in [1, 2, 3, 4, 5])  # Membership
print(a not in [1, 2, 3, 4, 5])  # Membership
print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Multiplication
print(a / b)  # Division
print(a % b)  # Modulus